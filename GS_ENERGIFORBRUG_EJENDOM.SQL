SELECT
				GS_BBRENHED.Adresse,
				JY74600V.KOMMUNENUMMER AS Kommunenummer,
				JY74600V.EJENDOMSNR AS Ejendomsnummer,
				JY74600V.VEJ_KODE AS Vejkode,
				JY74600V.HUS_NUMMER AS Husnummer,
				JY74600V.ETAGE AS Etage,
				JY74600V.SIDE_DOERNR AS Side_dørnr,
				JY74600V.BEBO_ARL AS Areal_til_beboelse,
				JY74600V.ENH_ERHV_ARL AS Areal_til_erhverv,
				JY74600V.FORSYNINGSART AS Forsyningsart,
				JY74600V.AARSTAL AS Årstal,
				JY74600V.FORBRUGMAENGDE AS Forbrugsmængde,
				JY74600V.ENTITETSTYPE AS Entitetstype,
				GS_BBRENHED.Anvendelseskode,
				GS_BBRENHED.Anvendelse,
				GS_BBRENHED.Boligtypekode,
				GS_BBRENHED.Boligtype,
				GS_BBRENHED.Enhedens_samlede_areal,
				GS_BBRENHED.Areal_til_beboelse AS Areal_til_beboelse2,
				GS_BBRENHED.Areal_til_erhverv AS Areal_til_erhverv2,
				GS_BBRENHED.Antal_værelser,
				GS_BBRENHED.Toiletforholdskode,
				GS_BBRENHED.Toiletforhold,
				GS_BBRENHED.Badeforholdskode,
				GS_BBRENHED.Badeforhold,
				GS_BBRENHED.Køkkenforholdskode,
				GS_BBRENHED.Køkkenforhold,
				GS_BBRENHED.Energiforsyningskode,
				GS_BBRENHED.Energiforsyning,
				GS_BBRENHED.Andet_areal,
				GS_BBRENHED.Varmeinstallationskode,
				GS_BBRENHED.Varmeinstallation,
				GS_BBRENHED.Opvarmningsmiddelskode,
				GS_BBRENHED.Opvarmningsmiddel,
				GS_BBRENHED.Supplerende_varmekode,
				GS_BBRENHED.Supplerende_varme,
				GS_BBRENHED.Antal_vandskyllede_toiletter,
				GS_BBRENHED.Antal_badeværelser,
				GS_BBRENHED.Bynavn,
				GS_BBRENHED.Postnummer,
				GS_BBRENHED.Postdistrikt,
				CASE
					WHEN GS_BBRENHED.ADRESSELINK_ID <> ''
					THEN GS_BBRENHED.ADRESSELINK_ID
					ELSE CAST(JY74600V.KOMMUNENUMMER AS VARCHAR(3)) + RIGHT('0000' + CONVERT(VARCHAR, JY74600V.VEJ_KODE), 4) + RTRIM(JY74600V.HUS_NUMMER)
				END AS ADRESSELINK_ID,
				CASE
					WHEN GS_BBRENHED.ADRESSE_ID <> ''
					THEN GS_BBRENHED.ADRESSE_ID
					ELSE CAST(JY74600V.KOMMUNENUMMER AS VARCHAR(3)) + CAST(JY74600V.VEJ_KODE AS VARCHAR(4)) + RTRIM(JY74600V.HUS_NUMMER)
				END AS ADRESSE_ID,
				CASE
					WHEN GS_BBRENHED.UDVadresseID <> ''
					THEN GS_BBRENHED.UDVadresseID
					ELSE CAST(JY74600V.KOMMUNENUMMER AS VARCHAR(3)) + CAST(JY74600V.VEJ_KODE AS VARCHAR(4)) + RTRIM(JY74600V.HUS_NUMMER) + LTRIM(JY74600V.ETAGE) + LTRIM(JY74600V.SIDE_DOERNR)
				END AS UDVadresseID,
				GS_BBRENHED.ENH_ID
FROM JY74600V LEFT OUTER JOIN
				GS_BBRENHED ON JY74600V.VEJ_KODE = GS_BBRENHED.Vejkode
				AND JY74600V.HUS_NUMMER = GS_BBRENHED.Husnummer
				AND JY74600V.ETAGE = GS_BBRENHED.ETAGE
				AND JY74600V.SIDE_DOERNR = GS_BBRENHED.Side_dørnr
ORDER BY GS_BBRENHED.Adresse, GS_BBRENHED.UDVadresseID