SELECT
				AG01000T.KOM_ID_OFF AS Kommunenummer,
				AG01000T.CVRNR AS CVR_nummer,
				CVR_PRODUKTIONSENHED.PNR,
				AG01000T.NAVN_1,
				AG01000T.NAVN_2,
				AG01000T.NAVN_3,
				AG01000T.NAVN,
				CVR_PRODUKTIONSENHED.Navn AS PNR_Navn,
				AG01000T.CO_NAVN_OFF AS CO_Navn,
				AG01000T.VEJKODE_OFF AS Vejkode,
				AG01000T.HUSNR_FRA_OFF,
				AG01000T.HUSNR_TIL_OFF,
				AG01000T.ADRESSE_OFF AS Officiel_adresse,
				AG01000T.POSTNR_OFF AS Postnummer,
				AG01000T.POSTDIST_OFF AS Postdistrikt,
				AG01000T.ENH_STARTDATO AS Startdato_CVR_P_nummer,
				AG01000T.BRANCHE AS JUR_Hovedbranchekode,
				QL57200V.TEKST AS JUR_Hovedbranche,
				AG01000T.BIBRANCHE_1 AS JUR_Bibranchekode_1,
				QL57200V_1.TEKST AS JUR_Bibranche_1,
				AG01000T.BIBRANCHE_2 AS JUR_Bibranchekode_2,
				QL57200V_2.TEKST AS JUR_Bibranche_2,
				AG01000T.BIBRANCHE_3 AS JUR_Bibranchekode_3,
				QL57200V_3.TEKST AS JUR_Bibranche_3,
				AG01000T.VIRKFORM_ID AS Virksomhedsformkode,
				QL57000V.TEKST AS Virksomhedsform,
				AG01000T.STR_KODE AS Størrelseskode,
				QL57100V.TEKST AS Virksomhedsstørrelse,
				AG01000T.ANT_ANS AS Antal_ansatte,
				AG01000T.TELEFONNUMMER AS Telefonnummer,
				AG01000T.TELEFAX AS Telefax,
				AG01000T.E_MAIL AS Email,
				AG01000T.KRED_OPL_TYPE AS Kreditoplysning,
				AG01000T.KRED_STATUS AS Kreditstatus,
				AG01000T.ENH_AJDATO AS Ajourf_dato_CVR_P_nummer,
				AG01000T.ENH_OPHOER_DATO AS Ophørsdato,
				AG01000T.KMD_AJOURFOER_TS AS Ajourføringdato_CVR_EogM,
				CVR_PRODUKTIONSENHED.CO_NAVN_OFF AS PNR_CO_Navn,
				CVR_PRODUKTIONSENHED.Officiel_adresse AS PNR_Officiel_adresse,
				CVR_PRODUKTIONSENHED.Postnummer AS PNR_Postnummer,
				CVR_PRODUKTIONSENHED.Postdistrikt AS PNR_Postdistrikt,
				CVR_PRODUKTIONSENHED.Hovedbranchekode AS PNR_Hovedbranchekode,
				CVR_PRODUKTIONSENHED.Hovedbranche AS PNR_Hovedbranche,
				CVR_PRODUKTIONSENHED.Bibranchekode_1 AS PNR_Bibranchekode_1,
				CVR_PRODUKTIONSENHED.Bibranche_1 AS PNR_Bibranche_1,
				CVR_PRODUKTIONSENHED.Bibranchekode_2 AS PNR_Bibranchekode_2,
				CVR_PRODUKTIONSENHED.Bibranche_2 AS PNR_Bibranche_2,
				CVR_PRODUKTIONSENHED.Bibranchekode_3 AS PNR_Bibranchekode_3,
				CVR_PRODUKTIONSENHED.Bibranche_3 AS PNR_Bibranche_3,
				CVR_PRODUKTIONSENHED.MATRIKELID,
				CVR_PRODUKTIONSENHED.ADRESSELINK_ID,
				CVR_PRODUKTIONSENHED.ADRESSE_ID,
				CVR_PRODUKTIONSENHED.ADG_ID
FROM
				AG01000T RIGHT OUTER JOIN
				CVR_PRODUKTIONSENHED ON AG01000T.CVRNR = CVR_PRODUKTIONSENHED.CVR_nummer LEFT OUTER JOIN
				QL57200V ON AG01000T.BRANCHE = QL57200V.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_1 ON AG01000T.BIBRANCHE_1 = QL57200V_1.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_2 ON AG01000T.BIBRANCHE_2 = QL57200V_2.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_3 ON AG01000T.BIBRANCHE_3 = QL57200V_3.KODE LEFT OUTER JOIN
				QL57000V ON AG01000T.VIRKFORM_ID = QL57000V.KODE LEFT OUTER JOIN
				QL57100V ON AG01000T.STR_KODE = QL57100V.KODE