SELECT
				A.P_NUMMER,
				A.CVR_ID AS CVR_nummer,
				A.NAVN_1_REDIGERET,
				A.NAVN_2_REDIGERET,
				A.NAVN_3_REDIGERET,
				A.NAVN_UREDIGERET,
				A.CO_NAVN,
				RTRIM(LTRIM(RTRIM(A.AVEJ_OFFADR) + ' ' + CONVERT(VARCHAR, A.EHUSFRA_OFFADR) + RTRIM(LTRIM(A.AHUSBFRA_OFFADR))  + ' ' + RTRIM(LTRIM(A.ETAGE_OFFADR)) + ' ' + RTRIM(LTRIM(A.SIDE_OFFADR)))) AS Officiel_adresse,
				A.EPOST_OFFADR AS Postnummer,
				A.HPOSTDIST_OFFADR AS Postdistrikt,
				A.ENH_STARTDATO AS Startdato_CVR_P_nummer,
				A.CVIRKBRANCE AS Hovedbranchekode,
				QL57200V.TEKST AS Hovedbranche,
				A.CVIRKBIBRANCE1 AS Bibranchekode_1,
				QL57200V_1.TEKST AS Bibranche_1,
				A.CVIRKBIBRANCE2 AS Bibranchekode_2,
				QL57200V_2.TEKST AS Bibranche_2,
				A.CVIRKBIBRANCE3 AS Bibranchekode_3,
				QL57200V_3.TEKST AS Bibranche_3,
				A.STR_KODE AS Størrelseskode,
				QL57100V.TEKST AS Virksomhedsstørrelse,
				A.ANTAL_ANSATTE,
				A.TELEFONNUMMER,
				A.TELEFAX,
				A.E_MAIL AS Email,
				A.ENH_AJDATO AS Ajourf_dato_CVR_P_nummer,
				A.DOPHOR AS Ophørsdato,
				A.DAJOUR_KMD AS Ajourføringdato_CVR_EogM,
				CONVERT(char(1), A.ARC)  AS Opdateringskode_CVR,
				A.EKOMNR_OFFADR AS Kommunenummer,
				A.CVEJK_OFFADR AS Vejkode,
				A.EHUSFRA_OFFADR AS Husnummer,
				A.AHUSBFRA_OFFADR AS Husbogstav,
				A.ETAGE_OFFADR AS Etage,
				A.SIDE_OFFADR AS Side_dørnr,
				JY72000V.ESREJDNR,
				CONVERT(varchar, JY72000V.LANDSEJERLAVKODE) + CONVERT(varchar, JY72000V.MATRNR) AS MATRIKELID,
				CONVERT(varchar, A.EKOMNR_OFFADR)  + RIGHT('0000' + CONVERT(VARCHAR, A.CVEJK_OFFADR), 4) + RIGHT('000' + CONVERT(VARCHAR, A.EHUSFRA_OFFADR), 3) + RTRIM(A.AHUSBFRA_OFFADR)  AS ADRESSELINK_ID,
				CONVERT(varchar, A.EKOMNR_OFFADR) + CONVERT(varchar, A.CVEJK_OFFADR) + RIGHT('000' + CONVERT(VARCHAR, A.EHUSFRA_OFFADR), 3) + RTRIM(A.AHUSBFRA_OFFADR) AS ADRESSE_ID,
				JY72000V.ADG_ID
FROM
				AG01100T AS A INNER JOIN
				AG01000T AS B ON A.CVR_ID = B.CVR_ID LEFT OUTER JOIN
				QL57200V ON A.CVIRKBRANCE = QL57200V.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_1 ON A.CVIRKBIBRANCE1 = QL57200V_1.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_2 ON A.CVIRKBIBRANCE2 = QL57200V_2.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_3 ON A.CVIRKBIBRANCE3 = QL57200V_3.KODE LEFT OUTER JOIN
				QL57100V ON A.STR_KODE = QL57100V.KODE LEFT OUTER JOIN
				JY72000V ON CONVERT(varchar, A.EKOMNR_OFFADR) + CONVERT(varchar, A.CVEJK_OFFADR) + RIGHT('000' + CONVERT(VARCHAR, A.EHUSFRA_OFFADR), 3)  + RTRIM(A.AHUSBFRA_OFFADR) = CONVERT(varchar, JY72000V.KOMMUNENUMMER) + CONVERT(varchar, JY72000V.VEJKODE)  + RTRIM(JY72000V.HUS_NR)
WHERE
				(A.EKOMNR_OFFADR = 336) AND (A.DOPHOR > { fn NOW() }) AND (B.DOPHOR > { fn NOW() }) AND (A.KMD_GYLDIG_TIL > { fn NOW() }) AND  (B.KMD_GYLDIG_TIL > { fn NOW() })