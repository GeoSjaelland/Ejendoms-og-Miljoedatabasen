SELECT
				JYC0100V.EKOMNR AS Kommunenummer,
				JYC0100V.CVR_ID AS CVR_nummer,
				JYC0100V.NAVN_1_REDIGERET,
				JYC0100V.NAVN_2_REDIGERET,
				JYC0100V.NAVN_3_REDIGERET,
				JYC0100V.NAVN_UREDIGERET,
				JYC0100V.CO_NAVN AS CO_Navn,
				JYC0100V.CVEJK AS Vejkode,
				JYC0100V.HUSNUMMER_FRA,
				JYC0100V.HUSNUMMER_TIL,
				JYC0100V.AVEJ_OFFADR AS Officiel_adresse,
				JYC0100V.EPOST_OFFADR AS Postnummer,
				JYC0100V.HPOSTDIST_OFFADR AS Postdistrikt,
				JYC0100V.DSTART_CVR_P_NUMME AS Startdato_CVR_P_nummer,
				JYC0100V.CVIRKBRANCE AS Hovedbranchekode,
				QL57200V.TEKST AS Hovedbranche,
				JYC0100V.CVIRKBIBRANCE1 AS Bibranchekode_1,
				QL57200V_1.TEKST AS Bibranche_1,
				JYC0100V.CVIRKBIBRANCE2 AS Bibranchekode_2,
				QL57200V_2.TEKST AS Bibranche_2,
				JYC0100V.CVIRKBIBRANCE3 AS Bibranchekode_3,
				QL57200V_3.TEKST AS Bibranche_3,
				JYC0100V.CVIRKFORM AS Virksomhedsformkode,
				QL57000V.TEKST AS Virksomhedsform,
				JYC0100V.CVIRKSTOR AS Størrelseskode,
				QL57100V.TEKST AS Virksomhedsstørrelse,
				JYC0100V.ANTAL_ANSATTE AS Antal_ansatte,
				JYC0100V.TELEFONNUMMER AS Telefonnummer,
				JYC0100V.TELEFAX AS Telefax,
				JYC0100V.E_MAIL AS Email,
				JYC0100V.CKREDITOPLYS AS Kreditoplysning,
				JYC0100V.CKREDITSTATUS AS Kreditstatus,
				JYC0100V.DAJOUR_CVR_P_NUMME AS Ajourf_dato_CVR_P_nummer,
				JYC0100V.DOPHOR AS Ophørsdato,
				JYC0100V.DAJOUR_KMD AS Ajourføringdato_CVR_EogM,
				JYC0100V.COPDATERING AS Opdateringskode_CVR,
				JY72000V.ESREJDNR, 
				JY72000V.LANDSEJERLAVKODE+JY72000V.KOMEJERLAVKODE AS MATRIKELID,
				JY72000V.MATRIKELBOGSTAV, 
				CAST(JYC0100V.EKOMNR AS varchar(3)) + RIGHT('0000' + CONVERT(VARCHAR, JYC0100V.CVEJK), 4) + RTRIM(JYC0100V.HUSNUMMER_FRA) AS ADRESSELINK_ID, 
				CAST(JYC0100V.EKOMNR AS varchar(3)) + CAST(JYC0100V.CVEJK AS varchar(4)) + JYC0100V.HUSNUMMER_FRA AS ADRESSE_ID
FROM 
				JYC0100V LEFT OUTER JOIN
				QL57200V ON JYC0100V.CVIRKBRANCE = QL57200V.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_1 ON JYC0100V.CVIRKBIBRANCE1 = QL57200V_1.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_2 ON JYC0100V.CVIRKBIBRANCE2 = QL57200V_2.KODE LEFT OUTER JOIN
				QL57200V AS QL57200V_3 ON JYC0100V.CVIRKBIBRANCE3 = QL57200V_3.KODE LEFT OUTER JOIN
				QL57000V ON JYC0100V.CVIRKFORM = QL57000V.KODE LEFT OUTER JOIN
				QL57100V ON JYC0100V.CVIRKSTOR = QL57100V.KODE LEFT OUTER JOIN
				JY72000V ON JYC0100V.EKOMNR = JY72000V.KOMMUNENUMMER AND JYC0100V.CVEJK = JY72000V.VEJKODE AND JYC0100V.HUSNUMMER_FRA = JY72000V.HUS_NR
WHERE 
				(JYC0100V.EKOMNR = 336)